package com.pelitabangsa.algoritma;

public class OOP {
  int a, b, result;

  public OOP (int x, int y) {
    a = x;
    b = y;
  }

  public void pertambahan () {
    result = a + b;
  }

  public void pengurangan () {
    result = a - b;
  }
  
  public void perkalian () {
    result = a * b;
  }
  
  public void pembagian () {
    result = a / b;
  }
}
